class Goal {
    public int year;
    public int month;
    public int day;
    public String name;
    public int bonus;
    public Goal(int year, int month, int day, String name, int bonus) {
        this.year = year;
        this.month = month;
        this.day = day;
        this.name = name;
        this.bonus = bonus;
    }
    public int getYear() {
        return year;
    }
    public int getMonth() {
        return month;
    }
    public int getDay() {
        return day;
    }
    public int getBonus() {
        return bonus;
    }
}